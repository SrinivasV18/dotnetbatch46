using System;

namespace ClassLibrary1
{
    public class MyMath
    {
        public int Sum(int a, int b)
        { return a + b; }
        public int Diff(int a, int b)
        {
            return a - b;
        }
        public int Multiply(int a, int b)
        {
            return a * b;
        }
    }

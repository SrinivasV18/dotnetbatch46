using System;

namespace MathLib
{
    public class Calcs
    {
        public int Sum(int a, int b)
        {
            return a + b;
        }
        public int Sum(int a, int b,int c)
        {
            return a + b +c;
        }
        public int Multiply(int a, int b)
        {
            return a * b;
        }
        
    }
}
